	General -bool true \
	OpenWith -bool true \
	Privileges -bool true

# Experimental: Use python's plistlib to modify the columns that show in List view
THIS_DIR="$(cd -- "$(dirname -- "${(%):-%N}")" && pwd)" # Get the directory where this script lives
